from enum import Enum


class ErrorCode(Enum):
    """
    Defines unique, application-wide error codes.
    These are organized by feature module for clarity.
    """

    # Generic Errors (0-999)
    INTERNAL_SERVER_ERROR = 0
    DATABASE_ERROR = 1

    # Auth Errors (1000-1999 )
    USER_NOT_FOUND = 1000
    USER_ALREADY_EXISTS = 1001
    INVALID_CREDENTIALS = 1002
    INSUFFICIENT_PERMISSIONS = 1003
    TOKEN_VALIDATION_ERROR = 1004
    TOKEN_EXPIRED = 1005
    TOKEN_INVALID_SCOPE = 1006
    TOKEN_MISSING_DATA = 1007
    INVALID_PASSWORD_FORMAT = 1008

    # AI Model Errors (2000-2999)
    MODEL_NOT_FOUND = 2000
    MODEL_LOAD_FAILED = 2001
    MODEL_CONFIG_ERROR = 2002

    # RAG  Errors (3000-3999)
    DOCUMENT_NOT_FOUND = 3000
    INGESTION_FAILED = 3001
    PDF_PARSING_FAILED = 3002
    UNSUPPORTED_FILE_TYPE = 3003

    # Tutor Errors (4000-4999)
    TUTOR_NOT_FOUND = 4000
    CHAT_NOT_FOUND = 4001
    CHAT_OWNERSHIP_REQUIRED = 4002
    MESSAGE_CREATION_FAILED = 4003
    TUTOR_OWNERSHIP_REQUIRED = 4004
    USER_NOT_ENROLLED = 4005
    SELF_ENROLLMENT_NOT_ALLOWED = 4006
    USER_ALREADY_ENROLLED = 4007
    INVITATION_EMAIL_MISMATCH = 4008
    INVITATION_NOT_FOUND = 4009
